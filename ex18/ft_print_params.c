void	ft_putchar(char c);

void	ft_print_params(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
}

int		main(int argc, char **argv)
{
	int k;

	k = 1;
	while (argc > 1 && k < argc)
	{
		ft_print_params(argv[k]);
		k++;
	}
	return (0);
}
